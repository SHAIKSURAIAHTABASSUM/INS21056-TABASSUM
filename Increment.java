package javapgms;
public class Increment {
	public static void main(String[] args) {
		int x=100;
		int y=200;
		
	//	System.out.println("the sum of x and y is..:"+(x+y));
		
		x=x+1;
		System.out.println(x);
		System.out.println(x++);
        System.out.println(y);
        System.out.println(y++);
		
		int i=24;
		int j=5;
		 
		
			
		System.out.println(i++);
		System.out.println(i);
		System.out.println(j++);
		System.out.println(j);
	}
}